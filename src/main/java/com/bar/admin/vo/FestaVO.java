package com.bar.admin.vo;

public class FestaVO {
	
	private int festaId;
	private String festaName;
	private String festaDate;
	private String festaPlace;
	private String festaDesc;
	private String festaImg;
	private String festaLink;
	
	public int getFestaId() {
		return festaId;
	}
	public void setFestaId(int festaId) {
		this.festaId = festaId;
	}
	public String getFestaName() {
		return festaName;
	}
	public void setFestaName(String festaName) {
		this.festaName = festaName;
	}
	public String getFestaDate() {
		return festaDate;
	}
	public void setFestaDate(String festaDate) {
		this.festaDate = festaDate;
	}
	public String getFestaPlace() {
		return festaPlace;
	}
	public void setFestaPlace(String festaPlace) {
		this.festaPlace = festaPlace;
	}
	public String getFestaDesc() {
		return festaDesc;
	}
	public void setFestaDesc(String festaDesc) {
		this.festaDesc = festaDesc;
	}
	public String getFestaImg() {
		return festaImg;
	}
	public void setFestaImg(String festaImg) {
		this.festaImg = festaImg;
	}
	public String getFestaLink() {
		return festaLink;
	}
	public void setFestaLink(String festaLink) {
		this.festaLink = festaLink;
	}
	
}
